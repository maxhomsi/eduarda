(function(){
  var STORAGE_KEY = 'er_lang';
  var currentLang = localStorage.getItem(STORAGE_KEY) || 'pt';

  function t(key){ return (TRANSLATIONS[currentLang] || TRANSLATIONS.pt)[key] || key; }

  function applyTranslations(){
    document.querySelectorAll('[data-i18n]').forEach(function(el){
      var key = el.getAttribute('data-i18n');
      if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA'){
        el.placeholder = t(key);
      } else {
        el.textContent = t(key);
      }
    });
    // Update html lang attribute
    document.documentElement.lang = currentLang === 'pt' ? 'pt-BR'
      : currentLang === 'en' ? 'en'
      : currentLang === 'es' ? 'es'
      : currentLang === 'fr' ? 'fr' : 'it';
    // Update active state on dropdown
    document.querySelectorAll('.lang-opt').forEach(function(el){
      el.classList.toggle('active', el.getAttribute('data-lang') === currentLang);
    });
    // Update button label
    var btn = document.querySelector('.lang-btn-label');
    if(btn) btn.textContent = currentLang.toUpperCase();
  }

  function setLang(lang){
    if(!TRANSLATIONS[lang]) return;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    applyTranslations();
  }

  // Wire up dropdown links
  document.addEventListener('DOMContentLoaded', function(){
    document.querySelectorAll('.lang-opt').forEach(function(el){
      el.addEventListener('click', function(e){
        e.preventDefault();
        setLang(el.getAttribute('data-lang'));
        // Close dropdown
        el.closest('.lang-drop').blur();
        document.activeElement && document.activeElement.blur();
      });
    });
    applyTranslations();
  });

  // Expose globally for inline use
  window.setLang = setLang;
})();
